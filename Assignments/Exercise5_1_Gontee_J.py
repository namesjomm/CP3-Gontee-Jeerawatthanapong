#Input
firstNumber = int(input("Enter the first number: "))
secondNumber = int(input("Enter the second number: "))

#Process
resultPlus = firstNumber + secondNumber
resultMinus = firstNumber - secondNumber
resultMultiplication = firstNumber * secondNumber
resultDivision = firstNumber / secondNumber

#Output
print(firstNumber,"+",secondNumber,"=",resultPlus)
print(firstNumber,"-",secondNumber,"=",resultMinus)
print(firstNumber,"*",secondNumber,"=",resultMultiplication)
print(firstNumber,"/",secondNumber,"=",resultDivision)